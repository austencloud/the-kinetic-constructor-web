// Function to get embedded arrow SVG by type, orientation, turns, and color
export function getEmbeddedArrowSvg(
	motionType: string,
	startOri: string,
	turns: number | string,
	color: string
): string | null {
	// Convert turns to string for comparison
	const turnsStr = typeof turns === 'number' ? turns.toString() : turns;

	// Determine if we're using radial or non-radial orientation
	const isRadial = startOri === 'in' || startOri === 'out';

	// Get the base SVG based on type, orientation and turns
	let baseSvg: string | null = null;

	// Handle float specially
	if (motionType === 'float' && turnsStr === 'fl') {
		baseSvg = FLOAT_SVG;
	}
	// Handle static arrows
	else if (motionType === 'static') {
		baseSvg = STATIC_SVG;
	}
	// Handle dash arrows
	else if (motionType === 'dash') {
		if (!isRadial) {
			// non-radial
			if (turnsStr === '0' || turnsStr === '0.0') {
				baseSvg = DASH_0_NONRADIAL_SVG;
			}
		}
	}
	// Handle pro arrows
	else if (motionType === 'pro') {
		if (isRadial) {
			// radial
			if (turnsStr === '0' || turnsStr === '0.0') {
				baseSvg = PRO_0_RADIAL_SVG;
			} else if (turnsStr === '0.5') {
				baseSvg = PRO_05_RADIAL_SVG;
			} else {
				// Fallback to non-radial for other turns
				if (turnsStr === '1' || turnsStr === '1.0') {
					baseSvg = PRO_1_NONRADIAL_SVG;
				} else if (turnsStr === '1.5') {
					baseSvg = PRO_15_NONRADIAL_SVG;
				}
			}
		} else {
			// non-radial
			if (turnsStr === '0' || turnsStr === '0.0') {
				baseSvg = PRO_0_NONRADIAL_SVG;
			} else if (turnsStr === '0.5') {
				baseSvg = PRO_05_NONRADIAL_SVG;
			} else if (turnsStr === '1' || turnsStr === '1.0') {
				baseSvg = PRO_1_NONRADIAL_SVG;
			} else if (turnsStr === '1.5') {
				baseSvg = PRO_15_NONRADIAL_SVG;
			}
		}
	}
	// Handle anti arrows
	else if (motionType === 'anti') {
		if (isRadial) {
			// radial
			// Fallback to non-radial for now
			if (turnsStr === '0' || turnsStr === '0.0') {
				baseSvg = ANTI_0_RADIAL_SVG;
			} else if (turnsStr === '0.5') {
				baseSvg = ANTI_05_RADIAL_SVG;
			} else if (turnsStr === '1' || turnsStr === '1.0') {
				baseSvg = ANTI_1_RADIAL_SVG;
			}
		} else {
			// non-radial
			if (turnsStr === '0' || turnsStr === '0.0') {
				baseSvg = ANTI_0_NONRADIAL_SVG;
			} else if (turnsStr === '0.5') {
				baseSvg = ANTI_05_NONRADIAL_SVG;
			} else if (turnsStr === '1' || turnsStr === '1.0') {
				baseSvg = ANTI_1_NONRADIAL_SVG;
			}
		}
	}

	// If we found a matching SVG, apply color
	if (baseSvg) {
		// Apply color transformation
		const hexColor = color === 'red' ? '#ED1C24' : '#2E3192';
		return baseSvg.replace(/\.st0{fill:#[0-9A-Fa-f]{6};}/g, `.st0{fill:${hexColor};}`);
	}

	return null;
}
